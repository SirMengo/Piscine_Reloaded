#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;
	int	calc;

	i = 0;
	while ((i * i) <= nb)
	{
		calc = i * i;
		if (calc == nb)
			return (i);
		i++;
	}
	return (0);
}
